package com.example.e_governance;

public class BirthRecord {
    public String Child_Name_Devanagari;
    public String Child_Name_English;
    public String Date_Of_Birth;
    public String Birth_place;
    public String Gender;
    public  String Cast;
    public String Number_of_child;
    public  String District;
    public String VDC;
    public String Ward_Number;
    public String Foreign_Country_Name;
    public String Grandfather_name_Devanagari;
    public String GrandFather_name_English;
    public  String Father_name_Devanagari;
    public String Father_name_English;
    public String Mother_name_Devanagari;
    public String Mother_name_English;

    public BirthRecord(String child_Name_Devanagari, String child_Name_English, String date_Of_Birth, String birth_place, String gender, String cast, String number_of_child, String district, String VDC, String ward_Number, String foreign_Country_Name, String grandfather_name_Devanagari, String grandFather_name_English, String father_name_Devanagari, String father_name_English, String mother_name_Devanagari, String mother_name_English) {
        this.Child_Name_Devanagari = child_Name_Devanagari;
        this.Child_Name_English = child_Name_English;
        this.Date_Of_Birth = date_Of_Birth;
        this.Birth_place = birth_place;
        this.Gender = gender;
        this.Cast = cast;
        this.Number_of_child = number_of_child;
        this.District = district;
        this.VDC = VDC;
        this.Ward_Number = ward_Number;
        this.Foreign_Country_Name = foreign_Country_Name;
        this.Grandfather_name_Devanagari = grandfather_name_Devanagari;
        this.GrandFather_name_English = grandFather_name_English;
        this.Father_name_Devanagari = father_name_Devanagari;
        this.Father_name_English = father_name_English;
        this.Mother_name_Devanagari = mother_name_Devanagari;
        this.Mother_name_English = mother_name_English;
    }
}
